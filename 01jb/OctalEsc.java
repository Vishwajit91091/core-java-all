class OctalEsc
{
	public static void main(String[] args) 
	{
		System.out.println("abc\0def");
		System.out.println("abc\1def");
		System.out.println("abc\2def");
		System.out.println("abc\3def");
		System.out.println("abc\4def");
		System.out.println("abc\5def");
		System.out.println("abc\6def");
		System.out.println("abc\7def");
		System.out.println("abc\\8def");
		System.out.println("abc\\9def");
		System.out.println("abc\10def");
		System.out.println("abc\11def");
		System.out.println("abc\12def");
	}
}
